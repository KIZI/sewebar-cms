<p>Quasar is a free GPL template release. Quasar makes use of the revolutionary <b>Gantry Joomla Template Framework</b>, developed exclusively by RocketTheme to enable flexible and highly extensible template development.</p>

<h2>What Is Gantry?</h2>

<p>Gantry is a sophisticated framework with the sole intention of being the best platform to build a solid theme with. Gantry takes all the lessons learned during the development of many RocketTheme Joomla templates and WordPress Themes and distills that knowledge into a single super-flexible framework that is easy to configure, simple to extend, and powerful enough to handle anything we want to throw at it.</p>

<h2>Key Features</h2>

<p>The Gantry framework is packed with many great features that enable the rapid development of feature-rich designs with a minimum amount of effort.  We've also gone to great lengths to enhance the standard Joomla administration user interface to make configuring a Gantry-powered template easier than ever before. Check out a sampling of the features Gantry brings to the table:</p>

<ul>
	<li>960 Grid System (http://960.gs) for simple consistent layout. 12 and 16 column support</li>
	<li>Per-menu control over all Gantry functionality</li>
	<li>Ability to save/remove custom presets</li>
	<li>Built-in AJAX Support</li>
	<li>RTL language support</li>
	<li>Built-in Gantry RokGZipper to compress and combine Gantry JS and CSS files</li>
	<li>Cleaner more understandable index.php.  All the complicated bits are handled for you in the Gantry core.</li>
	<li>Flexibility to configure up to 6 modules in row position.</li>
	<li>65 base module positions in the Gantry core.  These can be easily extended to add more</li>
	<li>36 possible combinations for the layout of the mainbody/sidebars alone!</li>
	<li>Flexibility to configure different grid-size layouts based on number of published modules.  You are not forced to use the same grid layout for every page</li>
	<li>12 built-in Gantry Features like Logo, font-sizer, to-top smooth slider.  Easily create your own to add more functionality with little coding required.</li>
	<li>Ability to force module display and 'blank' module positions for advanced module placement ability.</li>
	<li>Stunning new Admin interface providing better usability and control.</li>
	<li>Unique Layout controls allow you to configure your layout without having to do math or ever create a 'broken' layout.</li>
	<li>Layout controls provide visual representation of your front-end layout for simple and intuitive customization.</li>
	<li>Gantry is XML driven, adding another row of completely flexible module positions is as simple as editing one XML file.</li>
	<li>New flexible parameter system allows loading preset theme attributes and modifying for a custom design. Also support session and cookie level overriding for development.</li>
	<li>All new table-less HTML overrides based on the excellent GNU/GPLv2 overrides from YOOtheme (http://www.yootheme.com)
	<li>New standard typography and Joomla core elements styling</li>
	<li>New advanced caching system makes using Gantry really fast!</li>
	<li>All configuration state is stored in params.ini and cached for speed.</li>
	<li>Optimized codebase with speed, size, and reuse core tenants of the framework design</li>
</ul>

